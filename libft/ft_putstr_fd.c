#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (fd < 0 || !s)
		return ;
	else
	{
		while (s[i] != '\0')
			i++;
		write(fd, s, i);
	}
}
